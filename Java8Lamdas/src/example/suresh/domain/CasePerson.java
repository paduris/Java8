package example.suresh.domain;

public class CasePerson {

	private long casePersId;
	private long caseId;
	private long persId;

	public long getCasePersId() {
		return casePersId;
	}
	public void setCasePersId(long casePersId) {
		this.casePersId = casePersId;
	}
	public long getCaseId() {
		return caseId;
	}
	public void setCaseId(long caseId) {
		this.caseId = caseId;
	}
	public long getPersId() {
		return persId;
	}
	public void setPersId(long persId) {
		this.persId = persId;
	}

}
